def validacion_si_no(texto):

    continua= input("\n{} (s/n)? ".format(texto))
    
    while continua not in ["n", "N", "s", "S"]:
        print("\nEl valor ingresado no es correcto")
        
        continua= input("\n{} (s/n)? ".format(texto))        
        
    return continua


